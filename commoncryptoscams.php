            <h2>#1. Imposter Websites</h2>
            <p> You may be following a solid tip from someone with a lot of expertise but still become a victim by accidently visiting a fake website. There's a surprising number of websites that have been set up to resemble original, valid startup companies. If there isn't a small lock icon indicating security near the URL bar and no "https" in the site address think twice. Even if the site looks identical to the one you think you're visiting, you may find yourself directed to another platform for payment. For example, you click on a link that looks like a legitimate site, but attackers have created a fake URL with a zero in it instead of a letter ‘o’. That platform, of course, isn't taking you to the cryptocurrency investment that you've already researched. To avoid this, carefully type the exact URL into your browser. Double check it, too. </p>
          </div>
          <div class="scam_box">
            <img src="images/fakeapps.png" alt="Fake cryptocurrency scam app sample image">
            <h2>#2. Fake Mobile Apps</h2>
            <p> Another common way scammers trick cryptocurrency investors is through fake apps available for download through Google Play and the Apple App Store. Although stakeholders can often quickly find these fake apps and get them removed, that doesn't mean the apps aren't impacting many bottom lines. Thousands of people have already downloaded fake cryptocurrency apps, reports Bitcoin News. While this is a greater risk for Android users, every investor should be aware of the possibility. Are there obvious misspellings in the copy or even the name of the app? Does the branding look inauthentic with strange coloring or an incorrect logo? Take note and reconsider downloading. </p>
          </div>
          <div class="scam_box">
            <img src="images/badtweets.png" alt="Example of Warren Buffet being hacked and tweeting a malicious cryptocurrency address">
            <h2>#3. Bad Tweets and Other Social Media Updates</h2>
            <p> If you're following celebrities and executives on social media, you can't be sure that you're not following impostor accounts. The same applies to cryptocurrencies, where malicious, impersonating bots are rampant. Don't trust offers that come from Twitter or Facebook, especially if there seems to be an impossible result. Fake accounts are everywhere. If someone on these platforms asks for even a small amount of your cryptocurrency, it's likely you can never get it back. Just because others are replying to the offer, don't assume they aren't bots, either. You have to be extra careful. </p>
          </div>
          <div class="scam_box">
            <img src="images/scamemail.png" alt="A sample cryptocurrency phishing email">
            <h2>#4. Scamming Emails</h2>
            <p> Even if it looks exactly like an email you received from a legitimate cryptocurrency company, take care before investing your digital currency. Is the email the exact same, and are the logo and branding identical? Can you verify that the email address is legitimately connected to the company? The ability to check on this is one reason why it's important to choose a company that has real people working for it. If you have doubts about an email, ask someone who works there. And never click on a link in a message to get to a site. Scammers often announce fake ICOs, or initial coin offerings, as a way to steal substantial funds. Don't fall for these fake email and website offers. Take your time to look over all the details. Unfortunately, there are many ways that some Internet users exploit unsecure computing systems to mine or steal cryptocurrency. Learn more about staying safe and protecting yourself in this emerging market before you start investing in cryptocurrency. </p>
