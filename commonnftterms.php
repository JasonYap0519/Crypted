        <table class="terms">
          <tr>
            <td class="term">Apeing (into something)</td>
            <td class="definition"> Not affiliated with the Bored Ape Yacht Club or any of the other ape-themed 10k projects, to ‘ape into’ something means buying something in an irresponsible manner, out of FOMO, with too much money relative to your account size, and/or without having done your proper due diligence. </td>
          </tr>
          <tr>
            <td class="term">Diamond hands</td>
            <td class="definition"> ‘Diamond hands’ is one of the most popular slang terms in the world of crypto and NFTs. It’s become its own meme, its own emoji, and even the theme of whole NFT projects. A person with diamond hands is a person who holds on to an asset through price volatility, negative news, poor market sentiment, and whatever kind of FUD (another abbreviation we’ll cover below) you’ll encounter in this space. Diamond hands can also be used as a verb: “She diamond-handed bitcoin through the 2018 bear market.” </td>
          </tr>
          <tr>
            <td class="term">Floor</td>
            <td class="definition"> The ‘floor’ or ‘floor price’ of a project is simply the lowest price at which you can buy an NFT from that project on the secondary market. It’s the most popular metric for tracking a project’s performance over time and its relative success compared to others. </td>
          </tr>
          <tr>
            <td class="term">FUD</td>
            <td class="definition"> ‘FUD’ stands for Fear, Uncertainty, and Doubt. The term is used in crypto and NFTs alike to describe negative news stories, tweets, Discord messages, etc., that are either inaccurate or completely false. You’ll often see people point to FUD as the reason behind large price drops in cryptocurrencies or NFTs. </td>
          </tr>
          <tr>
            <td class="term">Gas war</td>
            <td class="definition"> Oh, the dreaded gas wars! These are a common occurrence on Ethereum and somewhat of a rite of passage for newcomers. When a popular NFT collection launches, with 100,000 people fighting for 10,000 NFTs, you need to increase your gas fee and outbid the others’ for your transaction to go through. This is what’s known as a gas war. </td>
          </tr>
          <tr>
            <td class="term">OS</td>
            <td class="definition"> OS is shorthand for OpenSea, the leading NFT marketplace on Ethereum. </td>
          </tr>
          <tr>
            <td class="term">Rugpull</td>
            <td class="definition"> A ‘rugpull’ is something I hope you’ll never experience! It’s essentially a scam where the people behind a seemingly legit project disappear with all the money immediately after launch. You might get an actual NFT in return for what you paid, but it’s most likely worthless and not even tradeable on a secondary marketplace. Rugpull is often used as a verb as well, as in “I got rugpulled” or simply “I got rugged”. You’ll also see some people on Discord simply writing ‘rug’, often to spread FUD in the community of a specific project. </td>
          </tr>
          <tr>
            <td class="term">(Floor) Sweep</td>
            <td class="definition"> ‘Sweeping’ is another commonly uttered term because of people’s interest in a project’s ‘floor’. To ‘sweep the floor’ means to buy up a large number of the cheapest NFTs in a collection on the secondary market. Teams sometimes do this, or are asked by the community to do this, for their own projects. Other times it may be done by a single individual, referred to as a ‘whale’. </td>
          </tr>
