          <div class="scam_box">
            <img src="images/rugpull.jpg" alt="A stock image of a graph breaking through the floor. Similar to a rugpull's graph">
            <h2>#1. Rug Pull Scams</h2>
            <p> A rug pull is a scam in which promoters of a project hype it up through social media channels suddenly stop backing it and take their investors’ money after the price has been driven up. As a result, the price of the NFT dramatically falls to zero, which leads to losses for investors in the NFT. A variant includes where developers of an NFT remove the ability to sell the token where purchasers will be unable to sell as the developers have added code removing this ability How to Avoid: The first thing to check is the developers behind the project to see if they are bona fide developers that have received good reviews over social media. If they have large followers but low engagement it could be a tell-tale sign. Use burner wallets where you can limit the number of funds you want to commit to a particular purchase-including crypto for transaction fees this limiting your risk. </p>
          </div>
          <div class="scam_box">
            <img src=" images/phishingscams.png" alt="Example of an OpenSea phishing email">
            <h2>#2. Phishing Scams</h2>
            <p> NFT phishing scam involves phony advertisements through fake websites and pop-ups that ask for users’ private wallet keys or 12-word security seed phrases. Once in possession of your wallet’s keys, scammers will hack into your wallet and deplete all the crypto and NFT collection stored in it. How to Avoid: Never give your wallet’s keys on pop-ups or suspicious websites. Always go directly to verified websites for crypto transactions, never use links, pop-ups, or your email to enter your wallet’s key information. </p>
          </div>
          <div class="scam_box">
            <img src="images/biddingscam.jpg" alt="Example of someone being scammed by a bidding scam bot">
            <h2>#3. Bidding scams</h2>
            <p> Bidding scams happen when investors want to resell their purchased NFTs in a secondary market. Once they list their NFT for sale in an NFT market, bidders might switch your preferred currency with low-valued cryptocurrencies without telling you, leading to potential losses for you. How to Avoid: Double-check the currency used before selling. </p>
          </div>
          <div class="scam_box">
            <img src="images/counterfeitnfts.png" alt="Example of a knock-off collection called 'Phunks' that flipped NFT art backwards and resold it.">
            <h2>#4. Counterfeit NFTs</h2>
            <p> This happens when scammers steal an artist’s work and open a fake on an NFT marketplace where they list counterfeit artwork for auction. Unsuspecting buyers will then make purchases on an NFT that has no value. How to Avoid: Before investing in an NFT from any marketplace, do your own research to make sure the NFT you are buying is from a verified account and is the real artist in question. Most legitimate NFT sellers will have a blue checkmark beside their usernames. </p>
          </div>
          <div class="scam_box">
            <img src="images/pumpanddump.png" alt="Example price graph of a pump and dump that was pulled on crypto users.">
            <h2>#5. Pump-and-dumps</h2>
            <p> Pump-and-dump schemes refer to when a group buys up NFTs thus artificially driving demand up. Unsuspecting investors believing that the NFTs have some value will join the auction and start bidding more. Once the bids go up, the scammers will sell off the NFTs for a profit leaving buyers with useless assets. How to Avoid: Review the transaction history of the NFT and contact information of the creator in question. Transactions that center around one date could indicate a pump and dump scheme </p>
